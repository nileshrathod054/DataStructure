package varibalesAndDataTypes;

public class MathOperation {

	public static void main(String[] args) {
		// Addition
		int a = 10;
		int b = 15;
		int sum = a + b;
		System.out.println("Addition of given numbers is: " + sum);

		// Multiplication
		int mult = a * b;
		System.out.println("Multiplication of given numbers is: " + mult);

		// Division
		int c = 9;
		int d = 3;
		int div = c / d;
		System.out.println("Division of given numbers is: " + div);

		// Subtraction
		int sub = b - a;
		System.out.println("Substraction of given numbers: " + sub);
	}
}
